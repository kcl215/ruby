webcamp = "プログラミング学習"
# webcamp = "プログラミング学習"　「変数名　=　入れたいデータ」
puts webcamp
# 実行結果　プログラミング学習　と出力される。

webcamp = "オンラインプログラミング学習"
puts webcamp

# 出力結果
# プログラミング学習
# オンラインプログラミンング学習

# 1つ目は3行目の、2つ目は7行目のそれぞれputsによる出力結果になります。
# 6行目で変数webcampに再度値を代入したため、5行目の結果はそれに代わっています。
# このように、変数の値は書き換えることができます。

# これが変数、上から順番に実行されていく中で
# 途中で値を書き換えることができる。

